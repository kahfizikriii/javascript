
// SOAL 2.1 - Metode Membuat Function


// 1. Function Declaration
function tambah(a, b) {
  return a + b;
}
console.log(tambah(2, 3)); // Output: 5

// 2. Function Expression
const kali = function(a, b) {
  return a * b;
};
console.log(kali(2, 3)); // Output: 6

// lanjutkan dengan Arrow Function, Constructor Function, Method dalam Object
